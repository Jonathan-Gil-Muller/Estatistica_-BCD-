# Definir funções para Estatística Descrita

def descritiva_quanti(dataset, variavel):
  """
  Parâmetros de entrada:
  dataset: um dataframe
  variável: nome de um variável "string"
  """
  # 1.Filtrar a variável analisada
  coluna = dataset[variavel]

  # 2.Calcular medidas de posição
  media   = round(np.nanmean(coluna), 1)
  mediana = round(np.nanmedian(coluna), 1)

  # 3.Calcular medidas de dispersão
  min = round(np.min(coluna), 1)
  max = round(np.max(coluna), 1)
  amp = max - min
  var = round(np.nanvar(coluna, ddof=1), 1)
  std = round(np.nanstd(coluna, ddof=1), 1)
  cv  = round((std/media) * 100, 1)

  # 4.Agrupar os resultados em um dicionário
  resultados = {
      'Variavel': variavel,
      'Media': media,
      'Mediana': mediana,
      'Mínimo': min,
      'Max': max,
      'Amplitude': amp,
      'Variância': var,
      'Desvio Padrão': std,
      'CV (%)': cv}

  # 5.Converter o dicionário em um DataFrame
  df_final = pd.DataFrame([resultados])

  # 6.Definir qual será a saída da função
  return df_final


def descritiva_quanli(dataset, variavel):
  """
  Parâmetros de entrada:
  dataset: um dataframe
  variável: nome de um variável "string"
  """
  # 1.Filtrar a variável analisada
  coluna = dataset[variavel]

  # 2.Calcular medidas de posição
  moda = statistics.multimode(coluna)

  # 3.Calcular medidas de dispersão
  ### Razão de Variação
  n   = len(coluna.dropna())
  f_m = coluna.value_counts().max() #frequência da moda
  rv  = round(1 - (f_m/n), 1)

  # 4.Agrupar os resultados em um dicionário
  resultados = {
      'Variavel': variavel,
      'Moda': moda,
      'RV': rv}

  # 5.Converter o dicionário em um DataFrame
  df_final = pd.DataFrame([resultados])

  # 6.Definir qual será a saída da função
  return df_final

# Calcular resumos estatísticos de dados quantitativos

