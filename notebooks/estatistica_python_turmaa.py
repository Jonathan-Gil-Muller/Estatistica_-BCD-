"""# **Assimetria e Curtose**"""

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

# Gerar 1000 números inteiros aleatórios entre 17 e 60
np.random.seed(42)   # Garante reprodutibilidade
valores = np.random.randint(low=17, high=60, size=1000)
valores = pd.Series(valores)

# Cálculo da Assimetria
assimetria = valores.skew()

# Cálculo da Curtose
curtose = valores.kurtosis()

print(f"Assimetria: {assimetria:.2f}")
print(f"Curtose: {curtose:.2f}")

from scipy.stats import gaussian_kde

# Criando um histograma
plt.hist(valores, bins=11, density=True, color='seagreen', edgecolor='black', alpha=0.5)

# Calculando e desenhando a curva de Densidade (KDE)
kde = gaussian_kde(valores)
eixo_x = np.linspace(min(valores), max(valores), 200) # Cria pontos do início ao fim do gráfico
plt.plot(eixo_x, kde(eixo_x), color='darkgreen', linewidth=2, label='Densidade (KDE)')

# Adicionando as linhas de Média e Mediana (Exatamente igual ao seu)
plt.axvline(valores.mean(), color='red', linestyle='--', label=f'Média: {valores.mean():.2f}')
plt.axvline(valores.median(), color='blue', linestyle='-', label=f'Mediana: {valores.median():.2f}')

# Personalização
plt.title(f'Análise da Forma da Distribuição\nAssimetria: {assimetria:.2f} | Curtose: {curtose:.2f}',
          fontsize=14, fontweight='bold')
plt.xlabel('Valores', fontsize=12)
plt.ylabel('Densidade', fontsize=12)
plt.legend()

plt.tight_layout()

plt.show()

